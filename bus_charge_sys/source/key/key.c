#include "key.h"


void key_init()
{
    P1SEL &= ~(1<<2);        //通用IO    
    P1DIR &= ~(1<<2);        //输入   
    P1INP &= ~(1<<2);        //上下拉模式
    
    P1SEL &= ~(1<<3);        //通用IO  
    P1DIR &= ~(1<<3);        //输入  
    P1INP &= ~(1<<3);        //上下拉模式
    
    //P1,P2,P3-->5,6,7
    P2INP &= ~(1<<6);       //上拉
}