#include "relay.h"


/*********************************************************************************************
* 名称：relay_init
* 功能：继电器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void relay_init(void)
{   
    P0SEL &= ~(1<<6);                                           //通用IO
    P0SEL &= ~(1<<7);                                           //通用IO
    
    P0DIR |= (1<<6);                                            //输出
    P0DIR |= (1<<7);                                            //输出
}


/*********************************************************************************************
* 名称：relay1_control
* 功能：继电器控制
* 参数：state:0-->关闭，1-->打开
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void relay1_control(u8 state)
{
    if(state)
    {
        P0_6 = 0;
    }
    else
    {       
        P0_6 = 1;
    }   
}


/*********************************************************************************************
* 名称：relay2_control
* 功能：继电器控制
* 参数：state:0-->关闭，1-->打开
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void relay2_control(u8 state)
{
    if(state)
    {
        P0_7 = 0;
    }
    else
    {       
        P0_7 = 1;
    }
}